def to_plain_dict(obj):
    """
    把 defaultdict 递归转成普通 dict，方便稳定导出 JSON。
    """
    if isinstance(obj, defaultdict):
        obj = dict(obj)

    if isinstance(obj, dict):
        return {
            key: to_plain_dict(value)
            for key, value in obj.items()
        }

    if isinstance(obj, list):
        return [
            to_plain_dict(item)
            for item in obj
        ]

    return obj


